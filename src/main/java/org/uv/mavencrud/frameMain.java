    private void showWindow(JInternalFrame component)
    {
        if (!Arrays.asList(desktop.getAllFrames()).contains(component))
        {
            int x = (desktop.getWidth() - component.getWidth()) / 2;
            int y = (desktop.getHeight() - component.getHeight()) / 2;
            
            desktop.add(component);
            component.setLocation(x, y);
        }
        
        component.setVisible(true);
    }

